    private void btn_GuardarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_GuardarActionPerformed
        // TODO add your handling code here:
        //Boton para registrar datos nuevos a la BD
        String usu = txt_Usuario.getText();
        String cont = String.valueOf(txtp_Clave.getPassword());
        String nom = txt_Nombre.getText();
        String ape = txt_Apellido.getText();
        String cor = txt_Correo.getText();
        String rol = String.valueOf(cmb_Rol.getSelectedIndex());

                
        if(usu.equals(""))
        {
            JOptionPane.showMessageDialog(null, "Agrega un Usuario");
        }
        
        else if(cont.equals(""))
        {
            JOptionPane.showMessageDialog(null, "Agrega una ContraseÃ±a");
        }      
        else if(revisarUsuarios(usu))
        {
            JOptionPane.showMessageDialog(null, "Este Usuario ya existe");
        }
        else{
        PreparedStatement ps;
        String query = "INSERT INTO `usuarios`(`Usuario`, `Clave`, `Nombre`, `Apellido`, `Email`, `Rol`) VALUES (?,?,?,?,?,?)";
        try {
            ps = MyConnection.getConnection().prepareStatement(query);
            
            ps.setString(1, usu);
            ps.setString(2, cont);
            ps.setString(3, nom);
            ps.setString(4, ape);
            ps.setString(5, cor);
            ps.setString(6, rol);
            if(ps.executeUpdate() > 0)
            {
                JOptionPane.showMessageDialog(null, "Nuevo Usuario Agregado");
                Limpiar();
            }
            
        } catch (SQLException ex) {
            Logger.getLogger(frm_Main.class.getName()).log(Level.SEVERE, null, ex);
            JOptionPane.showMessageDialog(null, "error "+ex);
            }
        } 
    }//GEN-LAST:event_btn_GuardarActionPerformed

